<p>San Quentin prison is a maximum security California state prison near San Rafael. Alcatraz Prison was a maximum-security federal prison, located on Alcatraz Island in San Francisco bay that closed in 1963. It has been decided that a triathlon between these two nefarious prisons would be a challenge for those who enjoy a challenge and attract history buffs as well. This particular race will be near Half Ironman, consisting of: a run of roughly 16 miles, a challenging swim of 3 miles and a scenic cycling course of 25 miles. The cost to enter is $100. The race will begin near San Quentin, run to Fisherma's wharf, swim to Alcatraz island and back and then bike around San Francisco bay, crossing the John T. Know freeway and ending where the race began. This is a race between historic locales and unique in its scope.</p>
